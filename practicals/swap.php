<?php
// Define two numbers
$num1 = 10;
$num2 = 20;

// Display the original values
echo "Original values: num1 = $num1, num2 = $num2<br>";

// Swap the numbers using a temporary variable
$temp = $num1;
$num1 = $num2;
$num2 = $temp;

// Display the swapped values
echo "Swapped values: num1 = $num1, num2 = $num2";
?>

